package hotelfx.helper.PrintModel;

/**
 *
 * @author Andreas
 */
public class ClassPrintDataGroup {
   String idGroup;
   String namaGroup;
   String keteranganGroup;

    public ClassPrintDataGroup() {
    }

    public String getIdGroup() {
        return idGroup;
    }

    public void setIdGroup(String idGroup) {
        this.idGroup = idGroup;
    }

    public String getNamaGroup() {
        return namaGroup;
    }

    public void setNamaGroup(String namaGroup) {
        this.namaGroup = namaGroup;
    }

    public String getKeteranganGroup() {
        return keteranganGroup;
    }

    public void setKeteranganGroup(String keteranganGroup) {
        this.keteranganGroup = keteranganGroup;
    }
}
