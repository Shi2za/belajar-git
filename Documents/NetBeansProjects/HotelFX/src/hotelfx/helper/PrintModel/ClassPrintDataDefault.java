package hotelfx.helper.PrintModel;

/**
 *
 * @author Andreas
 */
public class ClassPrintDataDefault {
    String tipeDefault;
    String namaDefault;
    String nilaiDefault;
    String keteranganDefault;

    public ClassPrintDataDefault() {
    }

    public String getTipeDefault() {
        return tipeDefault;
    }

    public void setTipeDefault(String tipeDefault) {
        this.tipeDefault = tipeDefault;
    }

    public String getNamaDefault() {
        return namaDefault;
    }

    public void setNamaDefault(String namaDefault) {
        this.namaDefault = namaDefault;
    }

    public String getNilaiDefault() {
        return nilaiDefault;
    }

    public void setNilaiDefault(String nilaiDefault) {
        this.nilaiDefault = nilaiDefault;
    }

    public String getKeteranganDefault() {
        return keteranganDefault;
    }

    public void setKeteranganDefault(String keteranganDefault) {
        this.keteranganDefault = keteranganDefault;
    }
    
    
}
