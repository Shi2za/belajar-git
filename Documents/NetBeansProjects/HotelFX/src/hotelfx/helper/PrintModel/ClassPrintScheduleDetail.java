package hotelfx.helper.PrintModel;

/**
 *
 * @author Andreas
 */
public class ClassPrintScheduleDetail {
    String tipeKaryawan;
    String idKaryawan;
    String namaKaryawan;
    String jabatanKaryawan;
    String jadwalKaryawan;
    String jamMasuk;
    String jamKeluar;

    public ClassPrintScheduleDetail() {
    }

    public String getTipeKaryawan() {
        return tipeKaryawan;
    }

    public void setTipeKaryawan(String tipeKaryawan) {
        this.tipeKaryawan = tipeKaryawan;
    }

    public String getIdKaryawan() {
        return idKaryawan;
    }

    public void setIdKaryawan(String idKaryawan) {
        this.idKaryawan = idKaryawan;
    }

    public String getNamaKaryawan() {
        return namaKaryawan;
    }

    public void setNamaKaryawan(String namaKaryawan) {
        this.namaKaryawan = namaKaryawan;
    }

    public String getJabatanKaryawan() {
        return jabatanKaryawan;
    }

    public void setJabatanKaryawan(String jabatanKaryawan) {
        this.jabatanKaryawan = jabatanKaryawan;
    }

    public String getJadwalKaryawan() {
        return jadwalKaryawan;
    }

    public void setJadwalKaryawan(String jadwalKaryawan) {
        this.jadwalKaryawan = jadwalKaryawan;
    }

    public String getJamMasuk() {
        return jamMasuk;
    }

    public void setJamMasuk(String jamMasuk) {
        this.jamMasuk = jamMasuk;
    }

    public String getJamKeluar() {
        return jamKeluar;
    }

    public void setJamKeluar(String jamKeluar) {
        this.jamKeluar = jamKeluar;
    }
}
